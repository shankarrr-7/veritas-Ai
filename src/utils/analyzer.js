/**
 * Main analysis function
 * @param {string} type - 'text' | 'image' | 'video' | 'url'
 * @param {string|File} data - The content to analyze (text string, File object, or URL string)
 * @param {function} onProgress - Progress callback
 */
export async function analyzeContent(type, data, onProgress) {
  const messages = SCAN_MESSAGES[type] || SCAN_MESSAGES.text
  const useGemini = isGeminiConfigured()

  // Show scanning animation
  const totalSteps = messages.length
  const stepDuration = useGemini ? 200 : 400 // Faster if Gemini handles the heavy lifting

  // Run progress animation and AI analysis concurrently
  const progressPromise = (async () => {
    for (let i = 0; i < totalSteps; i++) {
      if (onProgress) {
        onProgress({
          step: i + 1,
          total: totalSteps,
          message: messages[i],
          progress: ((i + 1) / totalSteps) * 100
        })
      }
      await new Promise(resolve => setTimeout(resolve, stepDuration))
    }
  })()

  let result = null

  // Try Gemini AI first
  if (useGemini) {
    const analysisPromise = (async () => {
      switch (type) {
        case 'text':
          return await analyzeTextWithGemini(data)
        case 'image':
          return await analyzeImageWithGemini(data)
        case 'video':
          return await analyzeVideoWithGemini(data)
        case 'url':
          return await analyzeURLWithGemini(data)
        default:
          return await analyzeTextWithGemini(data)
      }
    })()

    // Wait for both progress animation and analysis to complete
    const [_, geminiResult] = await Promise.all([progressPromise, analysisPromise])
    result = geminiResult
  } else {
    // Wait for progress animation to finish first
    await progressPromise
  }

  // Fallback to simulation if Gemini failed or wasn't configured
  if (!result) {
    switch (type) {
      case 'text':
        result = simulateTextAnalysis(data)
        break
      case 'image':
        result = simulateImageAnalysis(typeof data === 'object' ? data.name : data)
        break
      case 'video':
        result = simulateVideoAnalysis(typeof data === 'object' ? data.name : data)
        break
      case 'url':
        result = simulateURLAnalysis(data)
        break
      default:
        result = simulateTextAnalysis(data)
    }
  }

  return result
}

