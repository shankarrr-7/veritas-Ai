/**
 * Extract og:image from a webpage via CORS proxy
 */
async function extractOGImage(url) {
  try {
    const proxies = [
      `https://corsproxy.io/?${encodeURIComponent(url)}`,
      `https://api.allorigins.win/raw?url=${encodeURIComponent(url)}`,
    ]

    for (const proxyUrl of proxies) {
      try {
        const response = await fetch(proxyUrl, {
          headers: { 'Accept': 'text/html,*/*' },
          signal: AbortSignal.timeout(8000)
        })

        if (!response.ok) continue

        const html = await response.text()
        if (!html || html.length < 100) continue

        // Extract og:image
        const ogMatch = html.match(/<meta[^>]*property=["']og:image["'][^>]*content=["']([^"']+)["']/i) ||
                         html.match(/<meta[^>]*content=["']([^"']+)["'][^>]*property=["']og:image["']/i)

        if (ogMatch && ogMatch[1]) {
          let ogImageUrl = ogMatch[1]
          // Handle relative URLs
          if (ogImageUrl.startsWith('/')) {
            const urlObj = new URL(url)
            ogImageUrl = `${urlObj.protocol}//${urlObj.host}${ogImageUrl}`
          }
          console.log(`[Veritas AI] ✓ Extracted og:image: ${ogImageUrl.slice(0, 100)}...`)
          return ogImageUrl
        }

        // Try twitter:image as fallback
        const twitterMatch = html.match(/<meta[^>]*(?:name|property)=["']twitter:image(?::src)?["'][^>]*content=["']([^"']+)["']/i)
        if (twitterMatch && twitterMatch[1]) {
          console.log(`[Veritas AI] ✓ Extracted twitter:image: ${twitterMatch[1].slice(0, 100)}...`)
          return twitterMatch[1]
        }
      } catch (e) {
        continue
      }
    }

    return null
  } catch (err) {
    console.warn('[Veritas AI] Failed to extract og:image:', err.message)
    return null
  }
}

/**
 * Try to fetch text content from a webpage
 */
async function fetchPageText(url) {
  try {
    const proxies = [
      `https://corsproxy.io/?${encodeURIComponent(url)}`,
      `https://api.allorigins.win/raw?url=${encodeURIComponent(url)}`,
    ]

    for (const proxyUrl of proxies) {
      try {
        const response = await fetch(proxyUrl, {
          headers: { 'Accept': 'text/html,*/*' },
          signal: AbortSignal.timeout(8000)
        })

        if (!response.ok) continue

        const html = await response.text()
        if (!html) continue

        // Strip HTML tags and extract text
        const div = document.createElement('div')
        div.innerHTML = html

        // Remove scripts, styles, nav, footer
        div.querySelectorAll('script, style, nav, footer, header, aside, noscript').forEach(el => el.remove())

        // Get text from article, main, or body
        const article = div.querySelector('article') || div.querySelector('main') || div.querySelector('.content') || div
        const text = article.textContent?.replace(/\s+/g, ' ').trim()

        if (text && text.length > 50) {
          console.log(`[Veritas AI] ✓ Extracted ${text.length} chars of page text`)
          return text
        }
      } catch (e) {
        continue
      }
    }

    return null
  } catch (err) {
    console.warn('[Veritas AI] Failed to fetch page text:', err.message)
    return null
  }
}

