async function analyzeTextWithGemini(text) {
  if (!isGeminiConfigured()) return null

  try {
    const responseText = await callGeminiText(text, TEXT_ANALYSIS_PROMPT)
    if (!responseText) return null

    // Clean response — remove markdown code fences if present
    const cleaned = responseText.replace(/```json\s*/gi, '').replace(/```\s*/gi, '').trim()
    const analysis = JSON.parse(cleaned)

    // Compute text metrics locally
    const wordCount = text.split(/\s+/).filter(Boolean).length
    const sentenceCount = text.split(/[.!?]+/).filter(Boolean).length
    const uniqueWords = new Set(text.toLowerCase().split(/\s+/)).size
    const avgWordLength = text.replace(/\s/g, '').length / Math.max(wordCount, 1)
    const lexicalDiversity = uniqueWords / Math.max(wordCount, 1)

    return {
      type: 'text',
      aiProbability: clamp(analysis.aiProbability, 0, 100),
      humanProbability: clamp(analysis.humanProbability, 0, 100),
      confidence: clamp(analysis.confidence, 0, 100),
      summary: analysis.summary,
      patterns: (analysis.patterns || []).map(p => ({
        type: p.type || 'info',
        text: p.text
      })),
      explanation: analysis.explanation,
      metrics: {
        wordCount,
        sentenceCount,
        avgWordLength: Math.round(avgWordLength * 10) / 10,
        lexicalDiversity: Math.round(lexicalDiversity * 100) / 100,
        uniqueWords,
        ...(analysis.metrics || {})
      },
      chartData: generateChartData(analysis.aiProbability),
      poweredBy: 'Gemini AI'
    }
  } catch (error) {
    console.error('Gemini text analysis error:', error?.message || error)
    return null // Fallback to simulation
  }
}

