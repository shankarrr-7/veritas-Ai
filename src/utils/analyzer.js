function simulateTextAnalysis(text) {
  const wordCount = text.split(/\s+/).filter(Boolean).length
  const sentenceCount = text.split(/[.!?]+/).filter(Boolean).length
  const avgWordLength = text.replace(/\s/g, '').length / Math.max(wordCount, 1)
  const uniqueWords = new Set(text.toLowerCase().split(/\s+/)).size
  const lexicalDiversity = uniqueWords / Math.max(wordCount, 1)

  const hasRepetitivePatterns = lexicalDiversity < 0.5
  const isVeryUniform = avgWordLength > 5.5 || avgWordLength < 3.5
  const hasLongSentences = wordCount / Math.max(sentenceCount, 1) > 25

  let aiProbability = 35 + Math.random() * 30
  if (hasRepetitivePatterns) aiProbability += 15
  if (isVeryUniform) aiProbability += 10
  if (hasLongSentences) aiProbability += 8
  if (wordCount < 20) aiProbability -= 15
  if (wordCount > 500) aiProbability += 5
  aiProbability = clamp(aiProbability, 12, 95)

  const patterns = []
  if (hasRepetitivePatterns) patterns.push({ type: 'warning', text: 'Low lexical diversity detected — common in AI-generated text' })
  if (isVeryUniform) patterns.push({ type: 'warning', text: 'Unusually uniform word length distribution' })
  if (hasLongSentences) patterns.push({ type: 'info', text: 'Extended sentence structures detected' })
  if (lexicalDiversity > 0.7) patterns.push({ type: 'success', text: 'High lexical diversity — typical of human writing' })
  if (wordCount > 100) patterns.push({ type: 'info', text: `Analyzed ${wordCount} words across ${sentenceCount} sentences` })

  const confidence = 60 + Math.random() * 30

  return {
    type: 'text',
    aiProbability: round1(aiProbability),
    humanProbability: round1(100 - aiProbability),
    confidence: round1(confidence),
    summary: aiProbability > 60
      ? 'This content shows significant indicators of AI generation. The writing patterns, vocabulary distribution, and structural consistency suggest machine-generated text.'
      : aiProbability > 40
        ? 'This content shows mixed indicators. Some patterns suggest AI involvement while others are consistent with human authorship.'
        : 'This content appears predominantly human-written. The writing style shows natural variation and authentic linguistic patterns.',
    patterns,
    metrics: { wordCount, sentenceCount, avgWordLength: round1(avgWordLength), lexicalDiversity: Math.round(lexicalDiversity * 100) / 100, uniqueWords },
    explanation: generateExplanation('text', aiProbability),
    chartData: generateChartData(aiProbability),
    poweredBy: 'Heuristic Analysis (add Gemini API key for AI-powered results)'
  }
}

