/**
 * Detect what type of content a URL likely points to
 */
function detectURLContentType(url) {
  const u = url.toLowerCase()

  // Direct image URLs
  if (/\.(jpg|jpeg|png|gif|webp|bmp|svg|avif|tiff)(\?.*)?$/i.test(u)) return 'image'

  // Direct video URLs
  if (/\.(mp4|webm|mov|avi|mkv|m4v)(\?.*)?$/i.test(u)) return 'video'

  // Known image hosting platforms
  const imageHosts = [
    'imgur.com', 'i.imgur.com', 'i.redd.it', 'preview.redd.it',
    'pbs.twimg.com', 'images.unsplash.com',
    'th.bing.com', 'googleusercontent.com',
    'flickr.com', 'staticflickr.com',
    'pinimg.com', 'cdninstagram.com', 'scontent',
    'fbcdn.net'
  ]
  if (imageHosts.some(h => u.includes(h))) return 'image'

  // Instagram posts (contain images/videos)
  if (u.includes('instagram.com/p/') || u.includes('instagram.com/reel/')) return 'instagram'

  // Video platforms
  if (u.includes('youtube.com/watch') || u.includes('youtu.be/')) return 'youtube'
  if (u.includes('tiktok.com')) return 'tiktok'
  if (u.includes('vimeo.com')) return 'vimeo'
  if (u.includes('twitter.com') || u.includes('x.com')) return 'social'
  if (u.includes('facebook.com') || u.includes('fb.com')) return 'social'

  return 'webpage'
}

/**
 * Try to fetch a URL as an image and return base64
 */
async function fetchImageFromURL(url) {
  try {
    // Use a CORS proxy for cross-origin requests
    const proxies = [
      url, // Try direct first
      `https://corsproxy.io/?${encodeURIComponent(url)}`,
      `https://api.allorigins.win/raw?url=${encodeURIComponent(url)}`,
    ]

    for (const proxyUrl of proxies) {
      try {
        const response = await fetch(proxyUrl, {
          headers: { 'Accept': 'image/*,*/*' },
          signal: AbortSignal.timeout(10000)
        })

        if (!response.ok) continue

        const contentType = response.headers.get('content-type') || ''

        // Check if response is actually an image
        if (contentType.startsWith('image/') || contentType === 'application/octet-stream') {
          const blob = await response.blob()
          if (blob.size < 100) continue // Too small, probably an error page

          const mimeType = contentType.startsWith('image/') ? contentType.split(';')[0] : 'image/jpeg'
          const base64 = await blobToBase64(blob)

          console.log(`[Veritas AI] ✓ Fetched image from URL (${(blob.size / 1024).toFixed(1)} KB)`)
          return { base64, mimeType, size: blob.size }
        }
      } catch (e) {
        continue
      }
    }

    return null
  } catch (err) {
    console.warn('[Veritas AI] Failed to fetch image from URL:', err.message)
    return null
  }
}

/**
 * Try to get a thumbnail/preview image for video platform URLs
 */
function getVideoThumbnailURL(url) {
  const u = url.toLowerCase()

  // YouTube
  if (u.includes('youtube.com/watch') || u.includes('youtu.be/')) {
    let videoId = null
    if (u.includes('youtube.com/watch')) {
      const params = new URL(url).searchParams
      videoId = params.get('v')
    } else if (u.includes('youtu.be/')) {
      videoId = url.split('youtu.be/')[1]?.split(/[?&#]/)[0]
    }
    if (videoId) {
      // YouTube provides direct thumbnail access — try maxresdefault, then hqdefault
      return [
        `https://img.youtube.com/vi/${videoId}/maxresdefault.jpg`,
        `https://img.youtube.com/vi/${videoId}/hqdefault.jpg`,
        `https://img.youtube.com/vi/${videoId}/0.jpg`,
      ]
    }
  }

  // Vimeo (requires API call, use generic approach)
  if (u.includes('vimeo.com')) {
    const vimeoId = url.split('vimeo.com/')[1]?.split(/[?&#/]/)[0]
    if (vimeoId) {
      return [`https://vumbnail.com/${vimeoId}.jpg`]
    }
  }

  return null
}

/**
 * Fetch image from social media platforms using metadata extraction APIs
 * These APIs render the page with a headless browser, so they work with
 * JavaScript-rendered pages like Instagram, TikTok, Twitter, etc.
 */
async function fetchImageFromPlatform(url) {
  const services = [
    // Microlink — renders the page and extracts metadata
    async () => {
      const apiUrl = `https://api.microlink.io/?url=${encodeURIComponent(url)}`
      const res = await fetch(apiUrl, { signal: AbortSignal.timeout(15000) })
      if (!res.ok) return null
      const data = await res.json()
      const imgUrl = data?.data?.image?.url || data?.data?.screenshot?.url
      if (imgUrl) {
        console.log('[Veritas AI] ✓ Got image via Microlink:', imgUrl.slice(0, 80))
        return imgUrl
      }
      return null
    },
    // jsonlink.io — another metadata extraction service
    async () => {
      const apiUrl = `https://jsonlink.io/api/extract?url=${encodeURIComponent(url)}`
      const res = await fetch(apiUrl, { signal: AbortSignal.timeout(12000) })
      if (!res.ok) return null
      const data = await res.json()
      const imgs = data?.images
      if (imgs && imgs.length > 0) {
        console.log('[Veritas AI] ✓ Got image via jsonlink.io:', imgs[0].slice(0, 80))
        return imgs[0]
      }
      return null
    },
    // Open Graph scraper via noembed (works for some platforms)
    async () => {
      const apiUrl = `https://noembed.com/embed?url=${encodeURIComponent(url)}`
      const res = await fetch(apiUrl, { signal: AbortSignal.timeout(10000) })
      if (!res.ok) return null
      const data = await res.json()
      if (data?.thumbnail_url) {
        console.log('[Veritas AI] ✓ Got image via noembed:', data.thumbnail_url.slice(0, 80))
        return data.thumbnail_url
      }
      return null
    },
  ]

  for (const service of services) {
    try {
      const imageUrl = await service()
      if (imageUrl) return imageUrl
    } catch (e) {
      console.warn('[Veritas AI] Platform image fetch attempt failed:', e.message)
      continue
    }
  }

  return null
}

/**
 * Convert Blob to base64
 */
function blobToBase64(blob) {
  return new Promise((resolve, reject) => {
    const reader = new FileReader()
    reader.onloadend = () => resolve(reader.result.split(',')[1])
    reader.onerror = reject
    reader.readAsDataURL(blob)
  })
}

