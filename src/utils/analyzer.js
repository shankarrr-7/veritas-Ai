function clamp(val, min, max) {
  return Math.min(max, Math.max(min, Number(val) || 0))
}

function round1(val) {
  return Math.round(val * 10) / 10
}

function generateExplanation(type, probability) {
  const labels = { text: 'text content', image: 'image', video: 'video', url: 'web content' }
  const label = labels[type]

  if (probability > 70) {
    return `Our analysis engine detected strong indicators of AI generation in this ${label}. Multiple detection signals flagged synthetic patterns, unusual consistency, and markers typically associated with machine-generated content. While no detection system is 100% accurate, the confidence level suggests this content was likely created or significantly modified by AI.`
  } else if (probability > 45) {
    return `The analysis of this ${label} produced mixed results. Some indicators point toward AI involvement, while others suggest human authorship. This could indicate human-written content edited by AI, AI-generated content with human modifications, or naturally occurring patterns that coincidentally match AI markers.`
  } else {
    return `This ${label} shows predominantly human-authored characteristics. The natural variation in style, authentic patterns, and organic structure are consistent with human creation. Our detection algorithms found minimal indicators of AI generation.`
  }
}

function generateChartData(aiProbability) {
  const ai = Number(aiProbability) || 50
  return [
    { name: 'AI Patterns', score: Math.round(ai), fullMark: 100 },
    { name: 'Consistency', score: Math.round(clamp(40 + Math.random() * 50, 0, 100)), fullMark: 100 },
    { name: 'Complexity', score: Math.round(clamp(30 + Math.random() * 60, 0, 100)), fullMark: 100 },
    { name: 'Variation', score: Math.round(clamp(100 - ai + Math.random() * 20 - 10, 0, 100)), fullMark: 100 },
    { name: 'Authenticity', score: Math.round(100 - ai), fullMark: 100 },
    { name: 'Natural Flow', score: Math.round(clamp(100 - ai + Math.random() * 15, 0, 100)), fullMark: 100 }
  ]
}

