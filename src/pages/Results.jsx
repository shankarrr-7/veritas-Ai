import { useParams, Link } from 'react-router-dom'
import { motion } from 'framer-motion'
import { useScans } from '../context/ScanContext'
import CircularMeter from '../components/CircularMeter'
import {
  RadarChart, PolarGrid, PolarAngleAxis, PolarRadiusAxis,
  Radar, ResponsiveContainer, BarChart, Bar, XAxis, YAxis,
  CartesianGrid, Tooltip
} from 'recharts'
import {
  Shield, ArrowLeft, AlertTriangle, CheckCircle, Info,
  FileText, Image, Video, Link2, Download, Share2,
  Brain, BarChart3, Eye, Sparkles
} from 'lucide-react'

const typeIcons = {
  text: <FileText size={18} />,
  image: <Image size={18} />,
  video: <Video size={18} />,
  url: <Link2 size={18} />,
}

const typeLabels = {
  text: 'Text Analysis',
  image: 'Image Analysis',
  video: 'Video Analysis',
  url: 'URL Analysis',
}

export default function Results() {
  const { id } = useParams()
  const { scanHistory } = useScans()
  const scan = scanHistory.find(s => s.id === id)

  if (!scan) {
    return (
      <div className="pt-24 pb-16 min-h-screen">
        <div className="page-container text-center py-20">
          <Shield size={48} style={{ color: 'var(--text-tertiary)', margin: '0 auto 16px' }} />
          <h2 className="text-xl font-semibold mb-3" style={{ color: 'var(--text-primary)' }}>
            Scan Not Found
          </h2>
          <p className="text-sm mb-6" style={{ color: 'var(--text-secondary)' }}>
            This scan result may have been deleted or doesn't exist.
          </p>
          <Link to="/analyze" className="no-underline">
            <button className="btn-primary cursor-pointer">New Analysis</button>
          </Link>
        </div>
      </div>
    )
  }

  const { result } = scan

  const handleDownload = () => {
    const report = `VERITAS AI - ANALYSIS REPORT
================================

Scan ID: ${scan.id}
Date: ${new Date(scan.timestamp).toLocaleString()}
Type: ${typeLabels[scan.type] || scan.type}
Powered By: ${result.poweredBy || 'Heuristic Analysis'}

INPUT PREVIEW:
${scan.input}

--------------------------------
RESULTS

AI Probability: ${result.aiProbability}%
Human Probability: ${result.humanProbability}%
Confidence: ${result.confidence}%

SUMMARY:
${result.summary}

DETAILED EXPLANATION:
${result.explanation}

METRICS:
${Object.entries(result.metrics || {}).map(([key, val]) => `${key}: ${val}`).join('\n')}

PATTERNS DETECTED:
${result.patterns?.map(p => `- [${p.type.toUpperCase()}] ${p.text}`).join('\n') || 'None detected'}

================================
Generated by Veritas AI
`;

    const blob = new Blob([report], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `Veritas_AI_Report_${scan.id.slice(0, 8)}.txt`;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
  };

  return (
    <div style={{ paddingTop: '100px', paddingBottom: '80px' }} className="min-h-screen">
      <div className="page-container max-w-5xl">
        {/* Header */}
        <motion.div
          initial={{ opacity: 0, y: 20 }}
          animate={{ opacity: 1, y: 0 }}
          className="flex flex-col sm:flex-row items-start sm:items-center justify-between" style={{ marginBottom: '40px', gap: '16px' }}
        >
          <div className="flex items-center gap-4" style={{ minWidth: 0 }}>
            <Link to="/analyze" className="no-underline flex-shrink-0">
              <button className="w-10 h-10 rounded-xl flex items-center justify-center cursor-pointer border-none"
                style={{ background: 'var(--bg-tertiary)', color: 'var(--text-secondary)' }}>
                <ArrowLeft size={18} />
              </button>
            </Link>
            <div style={{ minWidth: 0 }}>
              <div className="flex items-center gap-2 mb-1" style={{ flexWrap: 'wrap' }}>
                <div className="w-7 h-7 rounded-lg flex items-center justify-center flex-shrink-0"
                  style={{ background: '#6366f115', color: '#6366f1' }}>
                  {typeIcons[result.type]}
                </div>
                <h1 className="text-xl font-bold" style={{ color: 'var(--text-primary)' }}>
                  {typeLabels[result.type]}
                </h1>
              </div>
              <p className="text-xs" style={{ color: 'var(--text-tertiary)' }}>
                {new Date(scan.timestamp).toLocaleString()}
              </p>
              {result.poweredBy && (
                <span className="inline-flex items-center gap-1 mt-1 px-2 py-0.5 rounded-md text-xs font-medium"
                  style={{
                    background: result.poweredBy.includes('Gemini') ? '#6366f115' : '#f59e0b15',
                    color: result.poweredBy.includes('Gemini') ? '#6366f1' : '#f59e0b'
                  }}>
                  <Sparkles size={10} />
                  {result.poweredBy}
                </span>
              )}
            </div>
          </div>

          <div className="flex items-center gap-2 flex-shrink-0">
            <button className="btn-secondary !py-2 !px-4 !text-xs flex items-center gap-1.5 cursor-pointer">
              <Share2 size={13} /> Share
            </button>
            <button onClick={handleDownload} className="btn-secondary !py-2 !px-4 !text-xs flex items-center gap-1.5 cursor-pointer">
              <Download size={13} /> Export
            </button>
          </div>
        </motion.div>

        {/* Main Grid */}
        <div className="grid grid-cols-1 lg:grid-cols-3" style={{ gap: '24px' }}>
          {/* Left Column - Meters */}
          <motion.div
            initial={{ opacity: 0, y: 20 }}
            animate={{ opacity: 1, y: 0 }}
            transition={{ delay: 0.1 }}
            className="lg:col-span-1 flex flex-col" style={{ gap: '20px' }}
          >
            {/* AI Probability */}
            <div className="glass-card p-6 text-center">
              <h3 className="text-sm font-semibold mb-5" style={{ color: 'var(--text-primary)' }}>
                AI Probability
              </h3>
              <div className="flex justify-center mb-4">
                <CircularMeter
                  percentage={result.aiProbability}
                  size={180}
                  strokeWidth={14}
                  label="AI Generated"
                />
              </div>
              <div className="flex items-center justify-center gap-6 mt-2">
                <div className="text-center">
                  <span className="text-lg font-bold block" style={{ color: result.aiProbability > 50 ? '#ef4444' : '#22c55e' }}>
                    {result.aiProbability}%
                  </span>
                  <span className="text-xs" style={{ color: 'var(--text-tertiary)' }}>AI</span>
                </div>
                <div className="w-px h-8" style={{ background: 'var(--border-color)' }} />
                <div className="text-center">
                  <span className="text-lg font-bold block" style={{ color: result.humanProbability > 50 ? '#22c55e' : '#ef4444' }}>
                    {result.humanProbability}%
                  </span>
                  <span className="text-xs" style={{ color: 'var(--text-tertiary)' }}>Human</span>
                </div>
              </div>
            </div>

            {/* Confidence Score */}
            <div className="glass-card p-6">
              <h3 className="text-sm font-semibold mb-3" style={{ color: 'var(--text-primary)' }}>
                Confidence Score
              </h3>
              <div className="flex items-center gap-3">
                <div className="flex-1 h-2.5 rounded-full overflow-hidden" style={{ background: 'var(--border-color)' }}>
                  <motion.div
                    className="h-full rounded-full"
                    style={{ background: 'var(--gradient-primary)' }}
                    initial={{ width: 0 }}
                    animate={{ width: `${result.confidence}%` }}
                    transition={{ duration: 1, delay: 0.5 }}
                  />
                </div>
                <span className="text-sm font-bold" style={{ color: 'var(--text-primary)' }}>
                  {result.confidence}%
                </span>
              </div>
              <p className="text-xs mt-3" style={{ color: 'var(--text-tertiary)' }}>
                Higher confidence means more reliable analysis
              </p>
            </div>

            {/* Verdict */}
            <div className="glass-card p-6" style={{
              borderColor: result.aiProbability > 60 ? '#ef444430' : result.aiProbability > 40 ? '#f59e0b30' : '#22c55e30'
            }}>
              <div className="flex items-center gap-2 mb-3">
                {result.aiProbability > 60 ? (
                  <AlertTriangle size={18} style={{ color: '#ef4444' }} />
                ) : result.aiProbability > 40 ? (
                  <Info size={18} style={{ color: '#f59e0b' }} />
                ) : (
                  <CheckCircle size={18} style={{ color: '#22c55e' }} />
                )}
                <h3 className="text-sm font-semibold" style={{ color: 'var(--text-primary)' }}>
                  Verdict
                </h3>
              </div>
              <p className="text-sm font-medium" style={{
                color: result.aiProbability > 60 ? '#ef4444' : result.aiProbability > 40 ? '#f59e0b' : '#22c55e'
              }}>
                {result.aiProbability > 60
                  ? 'Likely AI-Generated'
                  : result.aiProbability > 40
                    ? 'Mixed / Uncertain'
                    : 'Likely Human-Created'}
              </p>
            </div>
          </motion.div>

          {/* Right Column - Details */}
          <motion.div
            initial={{ opacity: 0, y: 20 }}
            animate={{ opacity: 1, y: 0 }}
            transition={{ delay: 0.2 }}
            className="lg:col-span-2 flex flex-col" style={{ gap: '20px' }}
          >
            {/* Summary */}
            <div className="glass-card p-6">
              <div className="flex items-center gap-2 mb-3">
                <Brain size={16} style={{ color: '#6366f1' }} />
                <h3 className="text-sm font-semibold" style={{ color: 'var(--text-primary)' }}>
                  Analysis Summary
                </h3>
              </div>
              <p className="text-sm leading-relaxed" style={{ color: 'var(--text-secondary)' }}>
                {result.summary}
              </p>
            </div>

            {/* Charts */}
            <div className="glass-card p-6">
              <div className="flex items-center gap-2 mb-4">
                <BarChart3 size={16} style={{ color: '#06b6d4' }} />
                <h3 className="text-sm font-semibold" style={{ color: 'var(--text-primary)' }}>
                  Analysis Breakdown
                </h3>
              </div>
              <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
                {/* Radar Chart */}
                <div className="rounded-xl p-4" style={{ background: 'var(--bg-tertiary)' }}>
                  <ResponsiveContainer width="100%" height={220}>
                    <RadarChart data={result.chartData}>
                      <PolarGrid stroke="var(--border-color)" />
                      <PolarAngleAxis
                        dataKey="name"
                        tick={{ fill: 'var(--text-tertiary)', fontSize: 10 }}
                      />
                      <PolarRadiusAxis
                        angle={30}
                        domain={[0, 100]}
                        tick={false}
                        axisLine={false}
                      />
                      <Radar
                        name="Score"
                        dataKey="score"
                        stroke="#6366f1"
                        fill="#6366f1"
                        fillOpacity={0.2}
                        strokeWidth={2}
                      />
                    </RadarChart>
                  </ResponsiveContainer>
                </div>

                {/* Bar Chart */}
                <div className="rounded-xl p-4" style={{ background: 'var(--bg-tertiary)' }}>
                  <ResponsiveContainer width="100%" height={220}>
                    <BarChart data={result.chartData} layout="vertical">
                      <CartesianGrid strokeDasharray="3 3" stroke="var(--border-color)" />
                      <XAxis type="number" domain={[0, 100]} tick={{ fill: 'var(--text-tertiary)', fontSize: 10 }} />
                      <YAxis dataKey="name" type="category" tick={{ fill: 'var(--text-tertiary)', fontSize: 10 }} width={80} />
                      <Tooltip
                        contentStyle={{
                          background: 'var(--bg-card)',
                          border: '1px solid var(--border-color)',
                          borderRadius: 8,
                          fontSize: 12
                        }}
                      />
                      <Bar dataKey="score" fill="#6366f1" radius={[0, 4, 4, 0]} />
                    </BarChart>
                  </ResponsiveContainer>
                </div>
              </div>
            </div>

            {/* Patterns */}
            <div className="glass-card p-6">
              <div className="flex items-center gap-2 mb-4">
                <Eye size={16} style={{ color: '#8b5cf6' }} />
                <h3 className="text-sm font-semibold" style={{ color: 'var(--text-primary)' }}>
                  Detected Patterns
                </h3>
              </div>
              <div className="flex flex-col gap-2.5">
                {result.patterns.map((pattern, i) => (
                  <motion.div
                    key={i}
                    initial={{ opacity: 0, x: -10 }}
                    animate={{ opacity: 1, x: 0 }}
                    transition={{ delay: 0.3 + i * 0.1 }}
                    className="flex items-start gap-3 p-3 rounded-xl"
                    style={{ background: 'var(--bg-tertiary)' }}
                  >
                    {pattern.type === 'warning' && <AlertTriangle size={14} style={{ color: '#f59e0b', marginTop: 2, flexShrink: 0 }} />}
                    {pattern.type === 'success' && <CheckCircle size={14} style={{ color: '#22c55e', marginTop: 2, flexShrink: 0 }} />}
                    {pattern.type === 'info' && <Info size={14} style={{ color: '#6366f1', marginTop: 2, flexShrink: 0 }} />}
                    <span className="text-sm" style={{ color: 'var(--text-secondary)' }}>
                      {pattern.text}
                    </span>
                  </motion.div>
                ))}
              </div>
            </div>

            {/* Metrics */}
            {result.metrics && (
              <div className="glass-card p-6">
                <div className="flex items-center gap-2 mb-4">
                  <Sparkles size={16} style={{ color: '#22c55e' }} />
                  <h3 className="text-sm font-semibold" style={{ color: 'var(--text-primary)' }}>
                    Content Metrics
                  </h3>
                </div>
                <div className="grid grid-cols-2 md:grid-cols-3 gap-3">
                  {Object.entries(result.metrics).map(([key, value]) => (
                    <div key={key} className="p-3 rounded-xl" style={{ background: 'var(--bg-tertiary)' }}>
                      <span className="text-xs block mb-1 capitalize" style={{ color: 'var(--text-tertiary)' }}>
                        {key.replace(/([A-Z])/g, ' $1').trim()}
                      </span>
                      <span className="text-sm font-semibold" style={{ color: 'var(--text-primary)' }}>
                        {typeof value === 'boolean' ? (value ? 'Yes' : 'No') : String(value)}
                      </span>
                    </div>
                  ))}
                </div>
              </div>
            )}

            {/* Why This Result */}
            <div className="glass-card p-6">
              <div className="flex items-center gap-2 mb-3">
                <Brain size={16} style={{ color: '#f59e0b' }} />
                <h3 className="text-sm font-semibold" style={{ color: 'var(--text-primary)' }}>
                  Why This Result?
                </h3>
              </div>
              <p className="text-sm leading-relaxed" style={{ color: 'var(--text-secondary)' }}>
                {result.explanation}
              </p>
            </div>

            {/* Actions */}
            <div className="flex flex-col sm:flex-row items-stretch gap-3">
              <Link to="/analyze" className="no-underline flex-1">
                <button className="btn-primary w-full flex items-center justify-center gap-2 cursor-pointer">
                  New Analysis
                </button>
              </Link>
              <Link to="/dashboard" className="no-underline flex-1">
                <button className="btn-secondary w-full flex items-center justify-center gap-2 cursor-pointer">
                  View Dashboard
                </button>
              </Link>
            </div>
          </motion.div>
        </div>
      </div>
    </div>
  )
}
